void    lcd_init(void)
{
	printf("lcd_nv3052: Setting up LCD pins\n");
        gpio_init(GPIO_LCD_RESET);
        gpio_init(GPIO_LCD_SDI);
        gpio_init(GPIO_LCD_SCLK);
        gpio_init(GPIO_LCD_CS);
        gpio_set_dir(GPIO_LCD_RESET, GPIO_OUT);
        gpio_set_dir(GPIO_LCD_SDI, GPIO_OUT);
        gpio_set_dir(GPIO_LCD_SCLK, GPIO_OUT);
        gpio_set_dir(GPIO_LCD_CS, GPIO_OUT);

	gpio_put(GPIO_LCD_CS, 1);
	gpio_put(GPIO_LCD_SCLK, 0);

	/* Reset it */
	gpio_put(GPIO_LCD_RESET, 1);
        sleep_ms(100);
        gpio_put(GPIO_LCD_RESET, 0);
        sleep_ms(100);
        gpio_put(GPIO_LCD_RESET, 1);

	printf("lcd_nv3052: LCD init commands\n");
	int ncmds = sizeof(init)/sizeof(initwr_t);
	for (int i = 0; i < ncmds; i++) {
		uint8_t cmd = init[i].cmd & 0xff;
		unsigned int delay = (init[i].cmd >> 16);
		uint8_t data = init[i].data;

		lcd_bbspi_cmd_data(cmd, data);
		sleep_ms(delay);
	}
	printf("lcd_nv3052: %d commands done\n", ncmds);
}
