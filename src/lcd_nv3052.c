typedef struct {
	unsigned int cmd; /* [7:0] = cmd, [31:16] = delay after cmd */
	uint8_t data;
} initwr_t;

/* Not using real SPI, just bitbang it; it's 9bit with DnC as MSB */

static void lcd_bbspi_tx(uint8_t pre, uint8_t data)
{
	gpio_put(GPIO_LCD_SCLK, 0);

	gpio_put(GPIO_LCD_SDI, !!pre); /* DnC */
	sleep_us(1);
	gpio_put(GPIO_LCD_SCLK, 1);
	sleep_us(1);
	gpio_put(GPIO_LCD_SCLK, 0);

	for (int i = 0; i < 8; i++) {
		gpio_put(GPIO_LCD_SDI, !!(data & 0x80));
		data <<= 1;
		sleep_us(1);
		gpio_put(GPIO_LCD_SCLK, 1);
		sleep_us(1);
		gpio_put(GPIO_LCD_SCLK, 0);
	}
}

static void lcd_bbspi_cmd_data(uint8_t cmd, uint8_t data)
{
	gpio_put(GPIO_LCD_CS, 1);
	gpio_put(GPIO_LCD_CS, 0);
	lcd_bbspi_tx(0, cmd);
	lcd_bbspi_tx(1, data);
	gpio_put(GPIO_LCD_CS, 1);
	sleep_us(1);
}

